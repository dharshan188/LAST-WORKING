    """Fetch steps and calories from Google Fit API"""
    try:
        service = build('fitness', 'v1', credentials=credentials)
        
        now = datetime.now()
        start_of_day = datetime(now.year, now.month, now.day, 0, 0, 0)
        
        start_time_nanos = int(start_of_day.timestamp() * 1e9)
        end_time_nanos = int(now.timestamp() * 1e9)
        
        fitness_data = {
            'steps': 0,
            'calories_burned': 0,
            'distance': 0,
            'active_minutes': 0
        }
        
        try:
            step_datasource = "derived:com.google.step_count.delta:com.google.android.gms:estimated_steps"
            steps_dataset = service.users().dataSources().datasets().get(
                userId='me',
                dataSourceId=step_datasource,
                datasetId=f"{start_time_nanos}-{end_time_nanos}"
            ).execute()
            
            if 'point' in steps_dataset:
                total_steps = sum(
                    point['value'][0]['intVal'] 
                    for point in steps_dataset['point'] 
                    if 'value' in point and len(point['value']) > 0
                )
                fitness_data['steps'] = total_steps
                print(f"‚úÖ Fetched {total_steps} steps")
            
            calories_datasource = "derived:com.google.calories.expended:com.google.android.gms:merge_calories_expended"
            calories_dataset = service.users().dataSources().datasets().get(
                userId='me',
                dataSourceId=calories_datasource,
                datasetId=f"{start_time_nanos}-{end_time_nanos}"
            ).execute()
            
            if 'point' in calories_dataset:
                total_calories = sum(
                    point['value'][0]['fpVal'] 
                    for point in calories_dataset['point'] 
                    if 'value' in point and len(point['value']) > 0
                )
                fitness_data['calories_burned'] = round(total_calories, 2)
                print(f"‚úÖ Fetched {total_calories:.2f} calories")
            
        except Exception as e:
            print(f"‚ùå Error fetching fitness data: {e}")
            traceback.print_exc()
        
        fitness_data['distance'] = round((fitness_data['steps'] * 0.75) / 1000, 2)
        fitness_data['active_minutes'] = round(fitness_data['steps'] / 100)
        
        return fitness_data
        
    except Exception as e:
        print(f"‚ùå Error in get_google_fit_data: {e}")
        traceback.print_exc()
        return {
            'steps': 0,
            'calories_burned': 0,
            'distance': 0,
            'active_minutes': 0,
            'error': str(e)
        }

