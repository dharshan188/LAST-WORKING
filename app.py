def calculate_daily_calories(food_items: List[Dict]) -> float:
    total = 0
    for item in food_items:
        name = item.get('name', '')
        qty_count = float(item.get('qty', 0))
        serving_size_g = get_serving_size(name)
        total_grams = serving_size_g * qty_count
        total += total_grams * 1.5
    return total

def predict_multi_timeline_health(profile: Dict, food_items: List[Dict], fitness_data: Dict = None, lang: str = 'en') -> Dict:
    try:
        current_weight = float(profile.get('weight', 70))
        height_cm = float(profile.get('height', 175))
        age = int(profile.get('age', 30))
        gender = profile.get('gender', 'Male')
        goal = profile.get('goal', 'Weight Maintenance')
        
        current_bp_sys = int(profile.get('systolicBP', 120))
        current_bp_dia = int(profile.get('diastolicBP', 80))
        current_sugar = int(profile.get('bloodSugar', 90))
        current_chol = int(profile.get('cholesterol', 180))
        
        if gender.lower() == 'male':
            bmr = 10 * current_weight + 6.25 * height_cm - 5 * age + 5
        else:
            bmr = 10 * current_weight + 6.25 * height_cm - 5 * age - 161
        
        if fitness_data:
            steps = fitness_data.get('steps', 0)
            if steps > 10000:
                activity_mult = 1.725
            elif steps > 7000:
                activity_mult = 1.55
            elif steps > 3000:
                activity_mult = 1.375
            else:
                activity_mult = 1.2
        else:
            activity_mult = 1.55
        
        tdee = bmr * activity_mult
        
        if fitness_data:
            tdee += fitness_data.get('calories_burned', 0) * 0.3
        
        daily_calories = calculate_daily_calories(food_items)
        calorie_diff = daily_calories - tdee
        
        total_carbs = sum(float(item.get('qty', 100)) * 0.3 for item in food_items)
        
        timelines = []
        
        for period_name, days in [("1 Week", 7), ("1 Month", 30), ("3 Months", 90)]:
            weight_change = (calorie_diff * days) / 7700
            predicted_weight = round(current_weight + weight_change, 1)
            
            sugar_change = 0
            if total_carbs > 300:
                sugar_change = (5 + (total_carbs - 300) * 0.02) * (days / 30)
            elif total_carbs < 150:
                sugar_change = -(150 - total_carbs) * 0.03 * (days / 30)
            
            if fitness_data and fitness_data.get('steps', 0) > 7000:
                sugar_change -= 2 * (days / 30)
            
            predicted_sugar = round(current_sugar + sugar_change, 1)
            
            bp_sys_change = 0
            bp_dia_change = 0
            if weight_change < -2:
                bp_sys_change = -3 * (days / 30)
                bp_dia_change = -2 * (days / 30)
            elif weight_change > 2:
                bp_sys_change = 3 * (days / 30)
                bp_dia_change = 2 * (days / 30)
            
            if fitness_data and fitness_data.get('steps', 0) > 7000:
                bp_sys_change -= 2 * (days / 30)
                bp_dia_change -= 1 * (days / 30)
            
            predicted_bp_sys = round(current_bp_sys + bp_sys_change)
            predicted_bp_dia = round(current_bp_dia + bp_dia_change)
            
            chol_change = 0
            if weight_change < -2:
                chol_change = -8 * (days / 30)
            elif weight_change > 2:
                chol_change = 5 * (days / 30)
            
            if fitness_data and fitness_data.get('steps', 0) > 7000:
                chol_change -= 3 * (days / 30)
            
            predicted_chol = round(current_chol + chol_change)
            
            timelines.append({
                "period": period_name,
                "days": days,
                "weight": {
                    "current": current_weight,
                    "predicted": predicted_weight,
                    "change": round(weight_change, 2),
                    "status": "success" if abs(weight_change) < 2 else ("warning" if abs(weight_change) < 4 else "danger")
                },
                "blood_sugar": {
                    "current": current_sugar,
                    "predicted": predicted_sugar,
                    "change": round(sugar_change, 2),
                    "status": "success" if abs(sugar_change) < 5 else ("warning" if abs(sugar_change) < 10 else "danger")
                },
                "blood_pressure": {
                    "current": f"{current_bp_sys}/{current_bp_dia}",
                    "predicted": f"{predicted_bp_sys}/{predicted_bp_dia}",
                    "change_sys": round(bp_sys_change, 1),
                    "change_dia": round(bp_dia_change, 1),
                    "status": "success" if abs(bp_sys_change) < 3 else ("warning" if abs(bp_sys_change) < 6 else "danger")
                },
                "cholesterol": {
                    "current": current_chol,
                    "predicted": predicted_chol,
                    "change": round(chol_change, 2),
                    "status": "success" if abs(chol_change) < 5 else ("warning" if abs(chol_change) < 10 else "danger")
                }
            })
        
        monthly_weight_change = timelines[1]["weight"]["change"]
        
        activity_note = ""
        if fitness_data:
            steps = fitness_data.get('steps', 0)
            if steps > 10000:
                activity_note = t('excellent_activity', lang)
            elif steps > 7000:
                activity_note = t('good_activity', lang)
            elif steps > 3000:
                activity_note = t('consider_more', lang)
            else:
                activity_note = t('low_activity_increase', lang)
        
        if goal == "Weight Loss":
            if monthly_weight_change < -1:
                overall = t('weight_loss_good', lang) + activity_note
            else:
                overall = t('weight_loss_bad', lang) + activity_note
        elif goal == "Weight Gain":
            if monthly_weight_change > 1:
                overall = t('weight_gain_good', lang) + activity_note
            else:
                overall = t('weight_gain_bad', lang) + activity_note
        else:
            if abs(monthly_weight_change) < 1:
                overall = t('maintenance_good', lang) + activity_note
            else:
                overall = t('maintenance_bad', lang) + activity_note
        
        return {
            "success": True,
            "daily_calories": round(daily_calories, 1),
            "tdee": round(tdee, 1),
            "calorie_diff": round(calorie_diff, 1),
            "goal": goal,
            "timelines": timelines,
            "overall_message": overall,
            "fitness_impact": fitness_data
        }
        
    except Exception as e:
        print(f"‚ùå Error in predict_multi_timeline_health: {e}")
        traceback.print_exc()
        return {
            "success": False,
            "error": str(e)
        }

