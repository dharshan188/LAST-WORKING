    weather = get_weather(city)
    
    fitness_data = None
    if 'credentials' in session:
        try:
            credentials = Credentials(**session['credentials'])
            fitness_data = get_google_fit_data(credentials)
        except Exception as e:
            print(f"‚ö†Ô∏è Could not fetch fitness data: {e}")

    totals_mg = {}
    processed_items = []
    
    for it in items:
        name = (it.get("name") or "").strip()
        try:
            qty_count = float(it.get("qty") or 0)
        except:
            qty_count = 0.0
        if not name or qty_count <= 0:
            continue
        
        serving_size_g = get_serving_size(name)
        total_grams = serving_size_g * qty_count
        
        processed_items.append({
            "name": name,
            "quantity": qty_count,
            "serving_size_g": serving_size_g,
            "total_grams": total_grams
        })
        
        print(f"üìä {name}: {qty_count} piece(s) = {total_grams}g (@ {serving_size_g}g/piece)")
        
        nut = get_food_nutrients(name)
        for full_name, (val, unit) in nut.items():
            try:
                actual = float(val) * (total_grams / 100.0)
            except:
                continue
            matched_key = None
            low = full_name.lower()
            for friendly, substrings in NUTRIENT_KEY_MAP.items():
                if any(s in low for s in substrings):
                    matched_key = friendly
                    break
            if not matched_key:
                continue
            amount_mg = convert_to_mg(actual, unit)
            totals_mg[matched_key] = totals_mg.get(matched_key, 0.0) + amount_mg

    defic = calculate_deficiency(totals_mg, gender, height, weight, fitness_data)
    rec = recommend_foods(defic, weather, fitness_data, lang)

    human_totals = {}
    for k, v in totals_mg.items():
        if k in ("Protein", "Fiber"):
            human_totals[k] = f"{round(v/1000.0, 2)} g"
        else:
            human_totals[k] = f"{round(v, 2)} mg"
    
    adjusted_nutrition = None
    if fitness_data:
        adjusted_nutrition = calculate_adjusted_nutrition(human_totals, fitness_data, lang)

    return jsonify({
        "weather": weather,
        "total_nutrients": human_totals,
        "deficient": defic,
        "recommendations": rec,
        "fitness_data": fitness_data,
        "adjusted_nutrition": adjusted_nutrition,
        "processed_items": processed_items
    })

