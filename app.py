def get_serving_size(food_name: str) -> int:
    food_lower = food_name.lower().strip()
    if food_lower in SERVING_SIZES:
        return SERVING_SIZES[food_lower]
    for key in SERVING_SIZES:
        if key in food_lower or food_lower in key:
            return SERVING_SIZES[key]
    return SERVING_SIZES["default"]

def get_food_nutrients(food: str) -> Dict[str, tuple]:
    url = "https://api.nal.usda.gov/fdc/v1/foods/search"
    params = {"api_key": USDA_API_KEY, "query": food, "pageSize": 1}
    try:
        r = requests.get(url, params=params, timeout=10)
        r.raise_for_status()
        data = r.json()
        nutrients = {}
        foods = data.get("foods", [])
        if not foods:
            return nutrients
        food_data = foods[0]
        for n in food_data.get("foodNutrients", []):
            name = n.get("nutrientName") or n.get("name")
            val = n.get("value")
            unit = n.get("unitName") or n.get("unit")
            if name and val is not None:
                try:
                    nutrients[name.strip()] = (float(val), unit or "")
                except Exception:
                    continue
        return nutrients
    except Exception as e:
        print(f"‚ùå USDA API error for {food}: {e}")
        return {}

def convert_to_mg(amount: float, unit: str) -> float:
    unit = (unit or "").lower()
    if unit in ("g", "gram", "grams"):
        return amount * 1000.0
    if unit in ("mg", "milligram", "milligrams"):
        return amount
    return amount

